type TranslationTree = typeof translations["zh-CN"];
type LeafPaths<T, Prefix extends string = ""> = {
  [K in keyof T]: T[K] extends string
    ? `${Prefix}${Extract<K, string>}`
    : LeafPaths<T[K], `${Prefix}${Extract<K, string>}.`>;
}[keyof T];

export type TranslationKey = LeafPaths<TranslationTree>;

export function useI18n() {
  const language = useConfigStore((state) => state.config?.language ?? "system");

  return useMemo(() => {
    const locale = resolveLocale(language);
    return {
      locale,
      language,
      t: (key: TranslationKey, vars?: Record<string, string | number>) =>
        translate(locale, key, vars),
    };
  }, [language]);
}

export function resolveLocale(language: string | undefined): Locale {
  const selected = toSupportedLocale(language);
  if (selected) return selected;

  const systemLanguages = navigator.languages?.length ? navigator.languages : [navigator.language];
  for (const systemLanguage of systemLanguages) {
    const matched = toSupportedLocale(systemLanguage);
    if (matched) return matched;
  }

  return "en";
}

function toSupportedLocale(language: string | undefined): Locale | null {
  const normalized = language?.toLowerCase();
  if (!normalized || normalized === "system") return null;
  if (normalized.startsWith("zh")) return "zh-CN";
  if (normalized.startsWith("en")) return "en";
  if (normalized.startsWith("ja")) return "ja";
  if (normalized.startsWith("ko")) return "ko";
  return null;
}

function translate(
  locale: Locale,
  key: TranslationKey,
  vars?: Record<string, string | number>
): string {
  const value = key.split(".").reduce<unknown>((current, part) => {
    if (current && typeof current === "object" && part in current) {
      return (current as Record<string, unknown>)[part];
    }
    return undefined;
  }, translations[locale]);

  const template = typeof value === "string" ? value : key;
  if (!vars) return template;

  return Object.entries(vars).reduce(
    (text, [name, replacement]) =>
      text.replace(new RegExp(`{{${escapeRegExp(name)}}}`, "g"), String(replacement)),
    template
  );
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
